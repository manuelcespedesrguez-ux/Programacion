package uno;

public class OpAritmeticos {
	
	public static void main(String[] args) {
	
	int n1=2, n2;
	
	n2=n1 * n1;
	System.out.println("numero 2 vale: "+ n2);// n2=4
	
	n2=n2-n1; 
	System.out.println("numero 2 vale: "+ n2);// n2=2
	
	n2=n2+n1+15; 
	System.out.println("numero 2 vale: "+ n2);// n2=19
	
	n2=n2/n1;
	System.out.println("numero 2 vale: "+ n2);// n2=9
	
	n2=n2%n1; 
	System.out.println("numero 2 vale: "+ n2);// n2=1

    }
}
