package uno;

public class primitivos {
	
	public static void main(String[] args) {
		
		byte  a=0;
		System.out.println("su valor es: " +a);
		
		short  b=0, c=3;
		System.out.println("su valor es: " +b);
		System.out.println("su valor es: " +c);
		
		int  d = -30;
		System.out.println("su valor es:" +d);
		
		int e = 0xC125;
		System.out.println("su valor es:" +e);
		
		long f=434123 ;
		System.out.println("su valor es:" +f);

		long g=5L ; /* la L en este caso indica Long*/
		System.out.println("su valor es:" +g);
	}
	
	
	
	
	
	
	

}
